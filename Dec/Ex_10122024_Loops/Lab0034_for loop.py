for i in range(0,10): # 0 to 9 , times ->10
    if i==5:
        print("Five")
    else:
        print(i)

# |i| condition| o/p
# |0| 0==5->False | o/p =0
# |1| 1==5->False | o/p =1
# |2| 2==5->False | o/p =2
# |3| 3==5->False | o/p =3
# |5| 5==5->False | o/p = Five
# |10| 10==5->False | o/p = For loop is finished

